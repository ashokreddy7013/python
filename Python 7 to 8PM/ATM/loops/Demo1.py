# using a dict in for
d1 = {101:"Ravi",102:"kumar",103:"krishna"}
print("Printing Only Keys")
for x in d1:
    print(x)
print("Thanks")

print("Printing values")
for x in d1.values():
    print(x)
print("Thanks")

print("Printing Keys and Values")
for x in d1.items():
    print(x)
print("Thanks")
print("---------------------")










